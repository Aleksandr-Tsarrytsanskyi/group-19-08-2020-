var t = null;
var f = 0;

// Таблица истинности логического И (&&):
//             true  && true = true
//             false && true = false
//             true  && false = false
//             false && false = false

console.log('and', t && f);

// Таблица истинности логического ИЛИ (||):
//             true  || true = true
//             false || true = true
//             true  || false = true
//             false || false = false


console.log('or',t || f);
